package org.example;

/*
 * 파일명과 클래스 이름이 같아야 한다
 * {} 블록을 사용해서 클래스와 메서드의 시작과 끝을 나타냄
 * public static void main(String[] args) main 메서드라 부름
 * 자바는 해당 메서드를 찾아 프로그램을 찾아 시작하므로, 프로그램의 시작점으로 보면 됨
 */
public class Main {
    public static void main(String[] args) {
        // 자바는 문자열 사용시 "" 쌍따옴표를 사용
        System.out.println("Hello world!");
    }
}